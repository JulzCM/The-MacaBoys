package ej_2;

public class Fecha {

	//Variables de instancia
	
		public String Mes;
		public String Dia;
		public int Año;
		
		public String getMes() {
			return Mes;
		}
		public void setMes(String txt) {
			Mes = txt;
		}
		public String getDia() {
			return Dia;
		}
		public void setDia(String txt) {
			Dia = txt;
		}
		public int getAño() {
			return Año;
		}
		public void setAño(int n) {
			Año = n;
		}
	
}
